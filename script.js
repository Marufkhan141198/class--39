//Determine summation.........

function sum (x,y)
{
    let digit1 = x;
    let digit2 = y;
    let result = digit1 + digit2;
    console.log("The sum  is " + result);
}
sum(20,30);
sum(120,30);

//Determine sub....

function sub (x,y)
{
    let digit1 = x;
    let digit2 = y;
    let result = digit1-digit2;
    console.log("The sub is "+ result);
}
sub(50,30);
sub(100,50);

